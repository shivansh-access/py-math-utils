def area_and_perimter_of_square(len):
    print("The area of given square of length " + str(len) + ":" + str(len*len))
    print("The perimeter of given square of length " + str(len) + ": " + str(len*4))


def area_and_perimter_of_rectangle(len, breadth):
    print("The area of given rectange of length " + str(len) + ": " + str(len * breadth))
    print("the primetre of the given rectangle " + str(breadth) + ": " + str((len + breadth) * 2)) 


def area_and_circumference_of_circle(radius):
    print("The area of given circle of radius " + str(radius) + ": " + str(3.14 * radius * radius))
    print("The circumference of the given circle of radius " + str(radius) + ": " + str(2 * 3.14 * radius))

